// jpg/png converters canonicalise to their merged hub page (Approach 2).
const CANONICAL_HUB: Partial<Record<ToolSlug, string>> = {
  "pdf-to-jpg": "pdf-to-image",
  "pdf-to-png": "pdf-to-image",
  "jpg-to-pdf": "images-to-pdf",
  "png-to-pdf": "images-to-pdf",
};

export function getLocalizedToolConfig(
  locale: Locale,
  slug: ToolSlug,
): PdfToolPageConfig {
  const base = {
    slug,
    locale,
    canonicalPath: localizedPath(locale, (CANONICAL_HUB[slug] ?? slug) as RouteSlug),
    alternateLanguages: languageAlternates(slug),
    ...localizedTools[locale][slug],
  };
  // For Chinese pages, substitute translated FAQ (zh configs inherit EN FAQ)
  if (locale === "zh" && zhFaq[slug]) {
    return { ...base, faqTitle: zhFaq[slug]!.faqTitle, faq: zhFaq[slug]!.faq };
  }
  if (locale === "en" && enFaq[slug]) {
    return { ...base, faqTitle: enFaq[slug]!.faqTitle, faq: enFaq[slug]!.faq };
  }
  return base;
}

export function getLocalizedToolUrl(locale: Locale, slug: ToolSlug) {
  return absoluteUrl(localizedPath(locale, slug));
}

